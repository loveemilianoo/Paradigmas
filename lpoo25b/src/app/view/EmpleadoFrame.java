    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        System.out.println("Modifica empleados");
        
        char sexo= 'F';
        if (rdbtnMasc.isSelected() == true){
            sexo = 'M';
        } else if(rdbtnFem.isSelected() == true){
            sexo = 'F';
        }

        int id = Integer.parseInt(txtId.getText());
        int edad = Integer.parseInt(txtEdad.getText().trim());

        Empleado empleado = new Empleado(txtNoEmpleado.getText(), txtRfc.getText(), txtCurp.getText(), txtNombre.getText(), edad, sexo, txtCorreo.getText());

        daoImp.modificarEmpleado(id, empleado);
        limpiar();
        cargarTabla();

        javax.swing.JOptionPane.showMessageDialog(this, "Empleado modificado");
    }//GEN-LAST:event_btnModificarActionPerformed

