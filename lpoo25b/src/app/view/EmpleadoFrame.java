    public void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaEmpleadoss.getModel();
        modeloTabla.setRowCount(0);
        
        int [] anchos = {50,50,50,90,50,40,150,70};
        
        for (int i =0 ; i< tablaEmpleadoss.getColumnCount(); i++){
            tablaEmpleadoss.getColumnModel().getColumn(1).setPreferredWidth(anchos[i]);
        }
        
        daoImp.construirTabla(modeloTabla);   
    }
    
