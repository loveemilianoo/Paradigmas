    public void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tablaClientes.getModel();
        modeloTabla.setRowCount(0);
        
        int [] anchos = {50,80,80,90,80,40,150,70,70,40,70};
        
        for (int i =0 ; i< tablaClientes.getColumnCount(); i++){
            tablaClientes.getColumnModel().getColumn(1).setPreferredWidth(anchos[i]);
        }
        
        daoImp.construirTabla(modeloTabla);   
    }
    
