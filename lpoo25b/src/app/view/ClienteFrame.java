    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        System.out.println("Guardar clientes: "+txtNombre.getText());
        
        char sexo= 'M';
        if (rbMasculino.isSelected() == true){
            sexo = 'M';
        } else if(rbFemenino.isSelected() == true){
            sexo = 'F';
        }
        
        int edad = Integer.parseInt(txtEdad.getText().trim());
        double saldo = Double.parseDouble(txtSaldo.getText().trim());
        
        Tarjeta tarjeta= new Tarjeta (txtNoTarjeta.getText(), txtNip.getText());
        Cuenta cuenta = new Cuenta(saldo, tarjeta );
        Cliente cliente= new Cliente(txtRfc.getText(), txtCurp.getText(), txtNombre.getText(), edad, sexo, txtCorreo.getText(), txtNoCliente.getText(), cuenta) ;
       
        daoImp.guardarCliente(cliente);
        cargarTabla();
        limpiarCampos();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente guardado");
    }//GEN-LAST:event_btnGuardarActionPerformed

