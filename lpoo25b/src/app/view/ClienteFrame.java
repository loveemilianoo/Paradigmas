    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        int fila = tablaClientes.getSelectedRow();
        int id = Integer.parseInt(tablaClientes.getValueAt(fila, 0).toString());
        
        Cliente cliente = daoImp.consultarCliente(id);
        
        if (cliente.getSexo() == 'M'){
            rbMasculino.setSelected(true);
        } else {
            rbFemenino.setSelected(true);
        }
        
        if (cliente.getCuenta() != null){
            txtSaldo.setText(String.valueOf(cliente.getCuenta().getSaldo()));
            if (cliente.getCuenta().getTarjeta() != null){
                txtNoTarjeta.setText(cliente.getCuenta().getTarjeta().getNoTarjeta());
                txtNip.setText(cliente.getCuenta().getTarjeta().getNip());
            }
        }
        
        txtId.setText(String.valueOf(id));
        txtRfc.setText(cliente.getRfc());
        txtCurp.setText(cliente.getCurp());
        txtNombre.setText(cliente.getNombre());
        txtEdad.setText(String.valueOf(cliente.getEdad()));
        txtCorreo.setText(cliente.getCorreo());
        txtNoCliente.setText(cliente.getNoCliente());
        
    }//GEN-LAST:event_tablaClientesMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        System.out.println("Modifica clientes");
        
        char sexo= 'M';
        if (rbMasculino.isSelected() == true){
            sexo = 'M';
        } else if(rbFemenino.isSelected() == true){
            sexo = 'F';
        }
        
        int id = Integer.parseInt(txtId.getText());
        int edad = Integer.parseInt(txtEdad.getText().trim());
        double saldo = Double.parseDouble(txtSaldo.getText().trim());
        
        Tarjeta tarjeta= new Tarjeta (txtNoTarjeta.getText(), txtNip.getText());
        Cuenta cuenta = new Cuenta(saldo, tarjeta );
        Cliente cliente= new Cliente(txtRfc.getText(), txtCurp.getText(), txtNombre.getText(), edad, sexo, txtCorreo.getText(), txtNoCliente.getText(), cuenta) ;
       
        daoImp.modificarCliente(id, cliente);
        cargarTabla();
        limpiarCampos();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente modificado");
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        System.out.println("Cliente Eliminado");
        
        int id = Integer.parseInt(txtId.getText());
        
        daoImp.eliminarCliente(id);
        cargarTabla();
        limpiarCampos();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente eliminado");
    }//GEN-LAST:event_btnEliminarActionPerformed

